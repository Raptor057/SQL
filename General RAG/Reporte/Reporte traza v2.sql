SELECT 
USH.ID AS [USH.ID]
,USH.UtcTimeStamp AS [USH.UTCTIMESTAMP]
,USH.SnapShotID AS [USH.SNAPSHOTID]
,USH.UnitID AS [USH.UNITID]
,USH.LineCode AS [USH.LINECODE]
,USH.PartNo AS [USH.PARTNO]
,USH.Revision AS [USH.REVISION]
,CS.ID AS [CS.ID]
,CS.SnapShotID AS [CS.SNAPSHOTID]
,CS.LineCode AS [CS.LINECODE]
,CS.PointOfUseCode AS [CS.POINTOFUSECODE]
,CS.EtiNoID AS [CS.ETINOID]
,CS.EtiNo AS [CS.ETINO]
,CS.MasterEtiNo AS [CS.MASTERETINO]
,CS.ComponentNo AS [CS.COMPONENTNO]
,CS.ComponentRev AS [CS.COMPONENTREV]
,CS.ComponentDescription AS [CS.COMPONENTDESCRIPTION]
,CS.LotNo AS [CS.LOTNO]
,CS.UtcTimeStamp AS [CS.UTCTIMESTAMP]
,POUE.ID AS [POUE.ID]
,POUE.PointOfUseCode AS [POUE.POINTOFUSECODE]
,POUE.EtiNo AS [POUE.ETINO]
,POUE.ComponentNo AS [POUE.COMPONENTNO]
,POUE.UtcEffectiveTime AS [POUE.UTCEFFECTIVETIME]
,POUE.UtcUsageTime AS [POUE.UTCUSAGETIME]
,POUE.UtcExpirationTime AS [POUE.UTCEXPIRATIONTIME]
,POUE.IsDepleted AS [POUE.ISDEPLETED]
,POUE.Comments AS [POUE.COMMENTS]
,POUE.LotNo AS [POUE.LOTNO]
  FROM [gtt].[dbo].[UnitIDShapshotHistory] USH
  LEFT JOIN [gtt].[dbo].[ComponentsSnapShot] CS ON USH.SnapShotID=CS.SnapShotID
  LEFT JOIN [gtt].[dbo].PointOfUseEtis POUE ON POUE.ID=CS.EtiNoID